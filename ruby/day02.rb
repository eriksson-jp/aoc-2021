class Submarine
	def initialize(horizontal_pos, depth)
		@horizontal_pos = horizontal_pos
		@depth = depth
	end
	
	def forward(amount)
		@horizontal_pos += amount
	end
	
	def down(amount)
		@depth += amount
	end
	
	def up(amount)
		@depth -= amount
	end
	
	def calculate()
		@horizontal_pos * @depth
	end
end

class AimingSubmarine
	def initialize(horizontal_pos, depth, aim)
		@horizontal_pos = horizontal_pos
		@depth = depth
		@aim = aim
	end
	
	def forward(amount)
		@horizontal_pos += amount
		@depth += amount * @aim
	end
	
	def down(amount)
		@aim += amount
	end
	
	def up(amount)
		@aim -= amount
	end
	
	def calculate()
		@horizontal_pos * @depth
	end
end

def parse_command(line)
	line.split(" ")
end

def perform_movement(submarine, input)
	input.each { | line |
		command = parse_command(line)
		case command[0]
			when "forward"
				submarine.forward(command[1].to_i)
			when "down"
				submarine.down(command[1].to_i)
			when "up"
				submarine.up(command[1].to_i)
			else
				raise "Unknown command #{command[0]}"
		end
	}
end

def taskOne(input)
	submarine = Submarine.new(0, 0)
	perform_movement(submarine, input)
	submarine.calculate()
end

def taskTwo(input)
	submarine = AimingSubmarine.new(0, 0, 0)
	perform_movement(submarine, input)
	submarine.calculate
end

puts "DAY 2.1: #{taskOne(input)}"
puts "DAY 2.2: #{taskTwo(input)}"